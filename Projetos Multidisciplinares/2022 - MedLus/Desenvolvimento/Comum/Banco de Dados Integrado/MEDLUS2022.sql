CREATE TABLE usuarios (
    USU_ID INTEGER AUTO_INCREMENT PRIMARY KEY,
    USU_CPF VARCHAR(12) NOT NULL UNIQUE,
    USU_RG VARCHAR(10) NOT NULL,
    USU_EMAIL VARCHAR(255) NOT NULL UNIQUE,
    USU_SENHA VARCHAR(20) NOT NULL,
    USU_NUMERO_CELULAR VARCHAR(13) NOT NULL,
    USU_TELEFONE VARCHAR(13),
    USU_DATA_DE_NASCIMENTO DATE NOT NULL,
    USU_NOME VARCHAR(100) NOT NULL,
    USU_NUMERO_RESIDENCIA VARCHAR(4) NOT NULL,
    USU_SEXO ENUM('M','F','O') NOT NULL,
    USU_CEP VARCHAR(8) NOT NULL,
    USU_COMPLEMENTO VARCHAR(100),
    USU_FOTO VARCHAR(255),
    USU_ATIVO BOOLEAN,
    USU_NOME_SOCIAL VARCHAR(100), 
    USU_HASH VARCHAR(60) DEFAULT NULL
);

CREATE TABLE pacientes (
    PAC_ID INTEGER AUTO_INCREMENT PRIMARY KEY,
    PAC_PRONTUARIO VARCHAR(6) NOT NULL UNIQUE,
    FK_USUARIOS_USU_ID INTEGER NOT NULL,
    FK_PLANOS_PLA_ID INTEGER NOT NULL
);

CREATE TABLE enfermeiros (
    ENF_ID INTEGER AUTO_INCREMENT PRIMARY KEY,
    ENF_EMAIL_PROFISSIONAL VARCHAR(255),
    ENF_PRONTUARIO VARCHAR(6) NOT NULL UNIQUE,
    ENF_COREN VARCHAR(255) NOT NULL UNIQUE,
    ENF_TELEFONE_PROFISSIONAL VARCHAR(13),
    FK_USUARIOS_USU_ID INTEGER
);

CREATE TABLE medicos (
    MED_ID INTEGER AUTO_INCREMENT PRIMARY KEY,
    MED_CRM VARCHAR(10) NOT NULL UNIQUE,
    MED_TELEFONE_PROFISSIONAL VARCHAR(13),
    MED_EMAIL_PROFISSIONAL VARCHAR(255),
    MED_PRONTUARIO VARCHAR(6) NOT NULL UNIQUE,
    MED_VALOR_CONSULTA DECIMAL(10,2) NOT NULL,
    MED_OBSERVACOES TEXT,
    MED_FORMACAO VARCHAR(50) NOT NULL,
    FK_USUARIOS_USU_ID INTEGER
);

CREATE TABLE secretarias (
    SEC_ID INTEGER AUTO_INCREMENT PRIMARY KEY,
    SEC_SETOR VARCHAR(50) NOT NULL,
    SEC_TELEFONE_PROFISSIONAL VARCHAR(13),
    SEC_PRONTUARIO VARCHAR(6) NOT NULL UNIQUE,
    SEC_EMAIL_PROFISSIONAL VARCHAR(255),
    FK_USUARIOS_USU_ID INTEGER
);

CREATE TABLE administradores (
    ADM_ID INTEGER AUTO_INCREMENT,
    ADM_EMAIL_PROFISSIONAL VARCHAR(255) NOT NULL,
    ADM_PRONTUARIO VARCHAR(6) NOT NULL UNIQUE,
    ADM_TELEFONE_PROFISSIONAL VARCHAR(13),
    FK_USUARIOS_USU_ID INTEGER,
    PRIMARY KEY (ADM_ID, FK_USUARIOS_USU_ID)
);

CREATE TABLE planos (
    PLA_ID INTEGER AUTO_INCREMENT PRIMARY KEY,
    PLA_AGENCIA VARCHAR(50) NOT NULL,
    PLA_CONTATO VARCHAR(100),
    PLA_PRECO DECIMAL(8,2) NOT NULL,
