        <!--Sobre o Projeto-->
        <h2 class="text-center"><strong>Sobre o Projeto</strong></h2>
        <div class="justify-content-md-center">
          <P>No ano de 2023, o projeto "Sustenta São João" foi designado aos alunos concluintes do curso Técnico em Informática Integrado ao Ensino Médio do Instituto Federal de Educação, Ciência e Tecnologia de São João da Boa Vista como uma proposta de Trabalho Final de Curso, 
            sendo este, direcionado para a população Sanjoanense, a fim de melhorar a comunicação entre os cidadãos e a prefeitura municipal, com foco na melhoria da qualidade de vida e na resolução de problemas relacionados ao saneamento básico e à gestão de resíduos sólidos, 
            promovendo a prática sustentável.</p>
          <p>O desenvolvimento deste projeto envolveu uma abordagem estruturada e colaborativa, onde uma turma foi dividida em cinco módulos distintos, cada um com funções específicas. Dentro de cada módulo, havia uma equipe composta por três especialistas, incluindo Analistas, DBA's (Administradores de Banco de Dados) e Desenvolvedores,
            aos quais foram designadas responsabilidades específicas, como o monitoramento do controle de versões SVN e o quadro de tarefas baseado na metodologia Kanban.
          </p>
        </div> 
        <br>

        <!--Sobre o Câmpus--><br>
        <h2 class="text-center"><strong>Sobre o Câmpus</strong></h2>
        <div class="text-center">
        <img class="text-center" src="\resources\img\IFSJBV.jpg">
        <br><br>
        <p class="justify-content-md-center">O Instituto Federal de Educação, Ciência e Tecnologia de São João da Boa Vista, conhecido como IFSP-SBV, é uma instituição de ensino pública localizada na cidade de São João da Boa Vista, no estado de São Paulo. Fundado em 2008, o IFSP-SBV faz parte da rede de Institutos Federais do Brasil e tem como missão oferecer educação de qualidade, promovendo o ensino, a pesquisa e a extensão, com foco nas áreas de ciência, tecnologia e educação profissional.</p>
        </div> <br>


